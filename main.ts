function clearLCD () {
    lcd.clearScreen()
    lcd.displayText("Test DC Motor", 2, 1)
}
l9110.pauseMotor(l9110.Motor.MotorA)
l9110.pauseMotor(l9110.Motor.MotorB)
basic.showIcon(IconNames.Snake)
basic.forever(function () {
    clearLCD()
    lcd.displayText("MTR A fwd 50%", 1, 2)
    l9110.controlMotor(l9110.Motor.MotorA, l9110.Rotate.Forward, 50)
    basic.pause(2000)
    clearLCD()
    lcd.displayText("STOP A", 1, 2)
    l9110.pauseMotor(l9110.Motor.MotorA)
    basic.pause(2000)
    clearLCD()
    lcd.displayText("MTR A bwd 50%", 1, 2)
    l9110.controlMotor(l9110.Motor.MotorA, l9110.Rotate.Backward, 50)
    basic.pause(2000)
    clearLCD()
    lcd.displayText("STOP A", 1, 2)
    l9110.pauseMotor(l9110.Motor.MotorA)
    basic.pause(2000)
    clearLCD()
    lcd.displayText("MTR B fwd 50%", 1, 2)
    l9110.controlMotor(l9110.Motor.MotorB, l9110.Rotate.Forward, 50)
    basic.pause(2000)
    clearLCD()
    lcd.displayText("STOP B", 1, 2)
    l9110.pauseMotor(l9110.Motor.MotorB)
    basic.pause(2000)
    clearLCD()
    lcd.displayText("MTR B bwd 50%", 1, 2)
    l9110.controlMotor(l9110.Motor.MotorB, l9110.Rotate.Backward, 50)
    basic.pause(2000)
    clearLCD()
    lcd.displayText("STOP B", 1, 2)
    l9110.pauseMotor(l9110.Motor.MotorB)
    basic.showIcon(IconNames.Yes)
    basic.pause(5000)
    basic.clearScreen()
})
